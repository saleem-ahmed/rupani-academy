    <div class="container mt-3">
        <div class="row">
            <div class="col-lg-6" style="text-align: justify">
                <h3 style="border-bottom:1px solid #B7B7B7">Diagnostic Assessment</h3>
                <p>
                    Rupani academy applies Diagnostic Assessment to evaluate student’s strengths, weaknesses, knowledge,
                    and skills before their unit of inquiry, at the beginning of a course, grade level, unit, or lesson.
                </p>
                <h4>
                    Tools for Diagnostic Assessment:
                </h4>
                <p>
                    The following tools are used for Diagnostic Assessment:
                    <li>
                        <b>a.</b> Mind map: mind mapping tool is used to connect key concepts of students by
                        brainstorming, using
                        images, flashcards, diagrams, puzzles, I spy game etc.
                    </li>
                    <li>
                        <b>b.</b> A short quiz: a short quiz is used such as, short test of knowledge e.g. MCQs, fill in
                        the
                        blanks, true/ false to assess student's prior knowledge.
                        c. Initial writing prompts: Initial writing prompts are used in which a text (or sometimes an
                        image)
                        that provides an idea or starting point for an original essay, report, story, poem, or other
                        forms
                        of writing of students is in practice.
                    </li>
                    <li>
                        <b> d.</b> Running Records: The academy uses the running records tool to assess the reading
                        level of
                        students.
                    </li>
                    <li>
                        <b>e.</b> Admission interview: The management of the Academy conducts face to face
                        interviews/discussion to
                        probe about children’s understanding of basic information and allow children to express them.
                    </li>
                    <li>
                        <b>f. </b> Admission test: Rupani Academy conducts admission test to gauge the basic
                        understanding and
                        information of the children which also provides a baseline data of the children before they
                        become a
                        part of Rupani Academy.
                    </li>
                </p>
                <h3 style="border-bottom:1px solid #B7B7B7">Summative Assessment</h3>
                <p>
                    Rupani academy uses summative assessment to evaluate student’s learning at the end of a unit of
                    inquiry by comparing it against standards or benchmarks designed by Rupani Academy. It is used by
                    teachers to inform parents about the quality of student’s learning, whether their classes are at
                    age-related expectations or not.
                </p>
                <h4>
                    Tools for Summative Assessment:
                </h4>
                <p>
                    The following tools are used for the Summative Assessment of students in the Academy:
                    <li>
                        <b> a. </b> Portfolios: Portfolios are developed to identify whether the students meet learning
                        standards and academic requirements for the courses and evaluate students’ work as a whole.
                    </li>
                    <li>
                        <b>b.</b> End-of-unit or chapter presentations: by the end of each chapter or unit, students are
                        asked for presentations in which their subject content, communication skills, confidence,
                        cooperation, and knowledge are assessed.
                    </li>
                </p>


            </div>
            <div class="col-lg-6" style="text-align: justify">
                <h3 style="border-bottom:1px solid #B7B7B7">Formative Assessment</h3>
                <p>
                    Rupani academy applies Formative Assessment to assess and observe students’ in an on- going way to
                    assess their learning to provide ongoing feedback, enhance their learning and identify skills
                    and concepts that students are struggling to understand, during the unit of inquiry, course, or in a
                    lesson.
                </p>
                <h4>
                    Tools for Formative Assessment:
                </h4>
                <p>
                    Rupani Academy uses the following tools ensure formative (continuous assessment) in the classes:
                    <li>
                        <b>a.</b> Observations: On going observation is used as a tool to observe all students by the
                        focus on
                        individuals, groups, or the whole class.
                    </li>
                    <li>
                        <b>b.</b> Tests/Quizzes: The Academy uses Tests/Quizzes to assess student’s knowledge or their
                        understanding during the learning process.
                    </li>
                    <li>
                        <b>c.</b> Projects: Students are involved in project work though which their participation,
                        understating
                        and achievements are assessed.
                    </li>
                    <li>
                        <b>d.</b> Portfolios: Portfolios are developed to identify whether the
                        students meet learning standards or other academic requirements for courses and to evaluate
                        their
                        work.
                    </li>
                    <li>
                        <b>e.</b> Exit tickets: Exit tickets are designed and implemented to assess students’
                        understanding of
                        learning material in class. Teachers can then use this data for adapting new strategies to meet
                        student’s needs.
                    </li>
                    <li>
                        <b>f.</b> Hand signals: Teachers use hand signals like thumb up, thumb down, and wave hand
                        responses
                        against their understanding of learning.
                    </li>
                    <li>
                        <b>g.</b> Classroom presentations: Through classroom presentations, students’ communication,
                        presentation
                        skills, confidence, and knowledge of the given tasks are assessed.
                    </li>
                    <li>
                        <b>h.</b> Rubrics: Rupani Academy uses rubrics to assess student's work, from written to oral
                        and visual.
                        It can be used for tracking assignments, activities, project work, fieldwork, class
                        participation,
                        and presentations.
                    </li>
                    <li>
                        <b>i.</b> Checklist: Checklist are developed and used to assess students’ level of understanding
                        in
                        content, participation and related to their performance.
                    </li>
                    <li>
                        <b>j.</b> Anecdotal records: Students’ specific behavior and actions are assessed through
                        anecdotal records
                        and also to identify possible developmental delays and progress.
                    </li>
                </p>
            </div>
